package oo.abstraction;

public class EletronicCar extends Car{

	public EletronicCar(String brand, int cc, String name, String type, int status) {
		super(brand, cc, name, type, status);
		this.brand = brand;
		this.cc = cc;
		this.name = name;
		this.type = type;
		this.status = status;
		
		
	}
	
	

}
