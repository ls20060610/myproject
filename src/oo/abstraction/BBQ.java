package oo.abstraction;

public class BBQ {
	String meat;
	String Part;
	String Place;
	String method;
	int Price;
	
	public BBQ(String meat, String Part, String Place, String method , int Price){
		this.meat = meat;
		this.Part = Part;
		this.Place = Place;
		this.method = method;
		this.Price = Price;
	
	}

}
