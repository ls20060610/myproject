package oo.abstraction;

public class Car {
	String brand;
	String name;
	String type;
	int cc;
	int status;
	
	public Car(String brand, int cc, String name, String type, int status){
		this.brand = brand;
		this.cc = cc;
		this.name = name;
		this.type = type;
		this.status = status;
	}

}
